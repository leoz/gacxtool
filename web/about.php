<h2>About</h2>

<p>
The GTK ACX Tool displays the status of the ACX driver.
</p>
<p>
This tool could be used just with driver developed by <a href="http://wlan.kewl.org">wlan.kewl.org Project</a>
on <a href="http://www.freebsd.org">FreeBSD</a> 5.2 and later versions.
</p>
<p>
The ACX driver could be <a href="install.html">installed</a> via the ports collection.
</p>
<p>
The GTK ACX Tool uses
<a href="http://www.gtk.org">GTK+ - The GIMP Toolkit</a>
and
<a href="http://www.eudoxos.de/gtk/gtkdatabox/">GtkDatabox - A GTK+ Widget for Fast Data Display</a>
.
</p>
<p>
Current functionality is mostly based on The ACXTool from
<a href="http://wlan.kewl.org">wlan.kewl.org Project</a>
and The acXmonitor from
<a href="http://www.houseofcraig.net">House of Craig</a>
.
</p>

